from typing import List


class Solution:
    def threeSum(self, nums: List[int]) -> List[List[int]]:
        # # n^4 풀이 (시간 초과)
        # answers = []
        # len_nums = len(nums)
        # for i in range(len_nums - 2):
        #     for j in range(i + 1, len_nums):
        #         for k in range(j + 1, len_nums):
        #             if nums[i] + nums[j] + nums[k] == 0:
        #                 answer = sorted([nums[i], nums[j], nums[k]])
        #                 if answer not in answers:
        #                     answers.append(answer)
        # return answers
        len_nums = len(nums)
        if len_nums < 3:
            return []
        nums.sort()
        answers = []
        i = 0
        # i는 0부터 마지막-2까지 간다
        while i < len_nums - 2:
            j = i + 1
            k = len_nums - 1
            # j와 k를 좁힌다
            while j != k:
                while k > j and nums[i] + nums[j] + nums[k] > 0:
                    k -= 1
                while j < k and nums[i] + nums[j] + nums[k] < 0:
                    j += 1
                if j != k and nums[i] + nums[j] + nums[k] == 0:
                    if [nums[i], nums[j], nums[k]] not in answers:
                        answers.append([nums[i], nums[j], nums[k]])
                    else:
                        j += 1
                if nums[i] == nums[k]:
                    break
            i += 1
        return answers



from typing import List


class Solution:
    def updateMatrix(self, mat: List[List[int]]) -> List[List[int]]:
        m = len(mat)  # rows
        n = len(mat[0])  # cols

        # 큐에 모든 0을 담아놓는다
        from collections import deque
        queue = deque()
        for r in range(m):
            for c in range(n):
                if mat[r][c]:
                    mat[r][c] = m + n
                else:
                    queue.append((r, c, 0))

        # 각 0에서 0이 아닌 칸으로 뻗어나간다
        while len(queue) > 0:
            r, c, dist = queue.popleft()
            mat[r][c] = min(mat[r][c], dist)
            if r + 1 < m and mat[r + 1][c] > dist + 1:
                queue.append((r + 1, c, dist + 1))
            if r - 1 >= 0 and mat[r - 1][c] > dist + 1:
                queue.append((r - 1, c, dist + 1))
            if c + 1 < n and mat[r][c + 1] > dist + 1:
                queue.append((r, c + 1, dist + 1))
            if c - 1 >= 0 and mat[r][c - 1] > dist + 1:
                queue.append((r, c - 1, dist + 1))

        return mat


